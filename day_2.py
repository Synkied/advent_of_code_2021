def compute_submarine_depth(moves):
    horizontal_moves = 0
    vertical_moves = 0
    aim = 0
    depth = 0

    for move in moves:
        direction, value = move.split('_')
        value = int(value)

        if direction == 'forward':
            horizontal_moves += value
            depth += aim * value

        elif direction == 'up':
            vertical_moves -= value
            aim -= value

        elif direction == 'down':
            vertical_moves += value
            aim += value

    part_1 = horizontal_moves * vertical_moves
    part_2 = horizontal_moves * depth
